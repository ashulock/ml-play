A = {1, 2, 3, 4}
B = {3, 4, 5, 6}
union = A.union(B)
intersection = A.intersection(B)
difference = A.difference(B)
symmetric_diff = A.symmetric_difference(B)
print("Union:", union)
print("Intersection:", intersection)
print("Difference A - B:", difference)
print("Symmetric Difference:", symmetric_diff)